    public void Init(){
   
        AVT.setText("");
        icon = EnglishFunnyFrame.scene.avatar[EnglishFunnyFrame.scene.user.getAvtID()];
        Image scaled = EnglishFunnyFrame.scene.scaleImage(icon.getImage(), AVT.getWidth(), AVT.getHeight());
        ImageIcon scaledIcon = new ImageIcon(scaled);
        AVT.setIcon(scaledIcon);
        
        TextField_Name.setForeground(xcolor);
        TextField_Name.setText(EnglishFunnyFrame.scene.user.getFullName());
        
        Email.setForeground(xcolor);
        Email.setText(EnglishFunnyFrame.scene.user.getEmail());
        
        PasswordField_Password.setForeground(xcolor);
        PasswordField_PassCom.setForeground(xcolor);
        PasswordField_PassCom.setText(EnglishFunnyFrame.scene.user.getPassWord());
        PasswordField_Password.setText(EnglishFunnyFrame.scene.user.getPassWord());
        
    }
    

