    private void Save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save_ButtonActionPerformed
        try{
            UserDAO userDAO = new UserDAO();
            String fullname = TextField_Name.getText();
            String email = Email.getText();
            String password = PasswordField_Password.getText();
            String Cpassword = PasswordField_PassCom.getText();

            if(password.equals(Cpassword) == false){
                JOptionPane.showMessageDialog(this,"Chưa đồng bộ mật khẩu");
                return;
            }

            
            UserModel userC = new UserModel(EnglishFunnyFrame.scene.user.getID(), EnglishFunnyFrame.scene.user.getUserName(), Cpassword,email, " ", fullname, 0 );
            EnglishFunnyFrame.scene.user = userC;
            userDAO.updateUser(userC);
            
        } catch (SQLException ex) {
            Logger.getLogger(ChangeUserInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Save_ButtonActionPerformed

